package com.corejava;

public class Firstjava {

	public static void main(String[] args) {
		int x=10;
		switch(x) {
		case 0:
			System.out.println(0);
			break;
			
		case 1:
			System.out.println(1);
		case 2:
			System.out.println(2);
		default:
			System.out.println("Default statement");
		}
		
		
		
		
	}

}
